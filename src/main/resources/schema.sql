drop table if exists product cascade;
drop table if exists member cascade;

create table product (
    id bigint generated by default as identity,
    name varchar(255),
    price int,
    imageUrl varchar(255),
    primary key (id)
);

create table member (
    id bigint generated by default as identity,
    name varchar(255),
    email varchar(255),
    password varchar(255),
    role varchar(255),
    primary key (id)
);