drop table if exists products;

-- for Product
create table products (
    id          bigint generated by default as identity,
    name        varchar(15),
    price       bigint,
    image_url   varchar(255),
    primary key (id)
);

-- for User
create table users (
    email       varchar(64) not null,
    password    varchar(64), -- store hashed pw
    permission  varchar(64),
    primary key (email)
);